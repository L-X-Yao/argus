const dicts: Record<Locale, Record<string, string>> = { zh, en };

export function t(key: string): string {
  return dicts[i18nState.locale][key] || dicts['zh'][key] || key;
}

export function setLocale(l: Locale) {
  i18nState.locale = l;
  try { localStorage.setItem('pllink_locale', l); } catch {}
  _syncCallback?.(l);
}

let _syncCallback: ((l: Locale) => void) | null = null;
export function onLocaleChange(cb: (l: Locale) => void) { _syncCallback = cb; }

export function getLocale(): Locale { return i18nState.locale; }

export function loadLocale() {
  try {
    const saved = localStorage.getItem('pllink_locale');
    if (saved === 'en' || saved === 'zh') i18nState.locale = saved;
  } catch {}
}
